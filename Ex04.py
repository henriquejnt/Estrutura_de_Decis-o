letra = input("Digite uma letra: ").lower()
if letra == 'a':
    print(f"A letra {letra} é uma vogal !")
elif letra == 'e':
    print(f"A letra {letra} é uma vogal !")
elif letra == 'i':
    print(f"A letra {letra} é uma vogal !")
elif letra == 'o':
    print(f"A letra {letra} é uma vogal !")
elif letra == 'u':
    print(f"A letra {letra} é uma vogal !")
else:
    print(f"A letra {letra} é uma consoante !")